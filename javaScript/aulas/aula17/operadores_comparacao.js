// Operadores de comparação

// > maior que
// < menor que
// >= maior ou igual a
// <= menor ou igual a

// == igual a
// === estritamente igual a
// != diferente de
// !== estritamente diferente de

console.log(50 > 12)
console.log(12 < 8)
console.log(4 >= 4)

console.log(2 == 2)
console.log(2 != 3)

